#Write a command to create vectors and get the number of elements in each vector.
import numpy as np

a = np.array([1,3,5,2,9])
b = np.array([-1,3,15,27,29])


print("a: ",a)
print("b: ",b)


print("a.size: ",a.size)
print("b.size: ",b.size)


print("a.ndim: ",a.ndim)
print("b.ndim: ",b.ndim)


print("a.shape: ",a.shape)
print("b.shape: ",b.shape)
